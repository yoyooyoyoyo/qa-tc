def thin_border():
    thin = Side(style="thin", color="CCCCCC")
    return Border(left=thin, right=thin, top=thin, bottom=thin)


def make_workbook():
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "testcases"

    # ── 헤더 ───────────────────────────────────
    headers = ["no.", "title", "테스트 진행 화면", "precondition",
               "steps", "expected result", "test-result", "comment", "issue ticket"]
    col_widths = [6, 60, 20, 40, 40, 50, 14, 40, 16]

    header_fill = PatternFill("solid", fgColor="2C5F8A")
    header_font = Font(name="맑은 고딕", bold=True, color="FFFFFF", size=10)
    center = Alignment(horizontal="center", vertical="center", wrap_text=True)
    wrap_left = Alignment(horizontal="left", vertical="top", wrap_text=True)

    for col_idx, (h, w) in enumerate(zip(headers, col_widths), start=1):
        cell = ws.cell(row=1, column=col_idx, value=h)
        cell.fill = header_fill
        cell.font = header_font
        cell.alignment = center
        cell.border = thin_border()
        ws.column_dimensions[get_column_letter(col_idx)].width = w

    ws.row_dimensions[1].height = 22

    # ── 데이터 행 ───────────────────────────────
    even_fill = PatternFill("solid", fgColor="F0F5FA")

    for row_idx, tc in enumerate(TCS, start=2):
        no, title, screen, pre, steps, expected, comment = tc
        row_data = [no, title, screen, pre, steps, expected, "", comment, ""]

        fill = even_fill if row_idx % 2 == 0 else None

        for col_idx, value in enumerate(row_data, start=1):
            cell = ws.cell(row=row_idx, column=col_idx, value=value)
            if fill:
                cell.fill = fill
            cell.font = Font(name="맑은 고딕", size=9)
            cell.alignment = wrap_left if col_idx != 1 else Alignment(
                horizontal="center", vertical="top")
            cell.border = thin_border()

        ws.row_dimensions[row_idx].height = 80

    # no. 컬럼 볼드
    for row_idx in range(2, len(TCS) + 2):
        ws.cell(row=row_idx, column=1).font = Font(
            name="맑은 고딕", size=9, bold=True)

    # 틀 고정
    ws.freeze_panes = "A2"

    wb.save(OUTPUT_PATH)
    print(f"✅ 저장 완료: {OUTPUT_PATH}")
    print(f"   총 {len(TCS)}개 TC 작성 (TC-001 ~ TC-037, TC-034~037 신규 추가)")


if __name__ == "__main__":
    make_workbook()
